import Foundation

//Convention de nomage :
//
//tous les noms seront en anglais et respectent le camelCase (première lettre minuscule puis chaque nouveau mot commence par une majuscule, le tout collé)
//
// class, struc, enum : commence par une majuscule
// parametre, variable, fonction : commence par une minuscule, se termine par un s pour les listes
// fonction : privilegié un verbe, une action
// écran : se termine par Screen, à placer dans le dossier Screens
//
//Utilisation des dossiers :
//
// ExtractedView : commence par Ext, à placer dans le dossier ExtractedViewList. Une extractedView par fichier
// Data : dossier qui contient les modèles de classe, les données et les fonctions
// Screens : dossier pour les fichiers de chaque écran
//
//Utilisation des fichiers :
//
// DatabaseX : fichier pour chaque collaborateur, à utiliser pour ranger ses variables
// Models : fichier pour lister les classes, les struct et les enums, les modifiers
// Functions : fichier pout stocker les fonctions
// XScreen : fichier d'un écran
// ExtX : fichier d'une ExtractedView
